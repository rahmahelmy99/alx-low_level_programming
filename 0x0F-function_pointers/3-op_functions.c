#include "3-calc.h"
/**
 * op_add - it returns the sum of a and b.
 * @a: input first number.
 * @b: input sec number.
 * Return: the result of sum.
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - it returns the sub of a and b.
 * @a: input first number.
 * @b: input sec number.
 * Return: the result of sub.
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - it returns the product of a and b.
 * @a: input first number.
 * @b: input sec number.
 * Return: the result of mul.
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - it returns the division of a and b.
 * @a: input first number.
 * @b: input sec number.
 * Return: the result of div.
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - it returns the remainder of the division of a and b.
 * @a: input first number.
 * @b: input sec number.
 * Return: the result of mod.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
